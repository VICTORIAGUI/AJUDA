# No python não é necessario especificar o tipo de váriave. Precisamos apenas declarar o nome.
nome = input('Digite seu nome: ') # Definindo a váriavel nome para receber o valor do input. Entrada de dados.

print('Bem vindo,', nome) # Saida de dados.


# Mais sobre váriaveis.

# No python podemos manipular variáveis básicas como strings (palavras ou cadeias de caracteres), inteiros e reais (float). Para criá-las, basta utilizar um comando de atribuição, que define seu tipo e seu valor.

mensagem = 'Exemplo de mensagem!' # String
n = 25 # Número 
pi = 3.141592653589931 #Float 

# Checando a class da váriavel.

print(type(mensagem)) #srt
print(type(n)) #int
print(type(pi)) #float 